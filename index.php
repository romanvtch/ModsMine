                            if (currentPage < totalPages) {
                                currentPage++;
                                window.location.href = "/page/" + currentPage;
                            }
                        });

                        function updatePaginationButtons() {
                            if (currentPage === 1) {
                                prevButton.disabled = true;
                                prevButton.style.visibility = "hidden";
