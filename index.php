                    function createPaginationButtons() {
                        const totalPages = Math.ceil(posts.length / postsPerPage);

                        prevButton.addEventListener('click', () => {
                            if (currentPage > 1) {
                                currentPage--;
                                window.location.href = "/page/" + currentPage;
